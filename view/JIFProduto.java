    private boolean validarCampos() {
        String msg = "";
        if (tfNome.getText().length() < 3) {
            msg += "\nO nome deve ter pelo menos 3 caracteres";
        }
        if (tfNome.getText().length() > 100) {
            msg += "\nO nome deve ter menos de 100 caracteres";
        }

        if (Integer.parseInt(tfValor.getText()) <= 0) {
            msg += "\nO valor do produto não pode ser 0";
        }
        if (msg.length() == 0) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Corrija os seguintes campos:" + msg,
                    "Meu sistema", JOptionPane.ERROR_MESSAGE);
            return false;
        }

    }

    private boolean preencherObjeto() {
        produto.setIdProduto(Integer.parseInt(lblValorID.getText()));
        produto.setNome_produto(tfNome.getText());
        produto.setValor_produto(Double.parseDouble(tfValor.getText()));
        return true;
    }

