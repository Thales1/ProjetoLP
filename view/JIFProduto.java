    private void tfPesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfPesquisaKeyReleased
        try {
            DefaultTableModel mp = (DefaultTableModel) tableProdutos.getModel();
            mp.setNumRows(0);
            if (tfPesquisa.getText().length() > 1) {
                listaDeProdutos = produtoData.pesquisar(tfPesquisa.getText());
                for (Produto p : listaDeProdutos) {
                    mp.addRow(new String[]{"" + p.getIdProduto(), p.getNome_produto(), "" + p.getValor_produto()});
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro:" + e.getMessage(), "Erro ao pesquisar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tfPesquisaKeyReleased

    private void tableProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProdutosMouseClicked
        try {
            int linhaSelecionada = tableProdutos.getSelectedRow();
            if (linhaSelecionada > -1) {
                lblValorID.setText("" + listaDeProdutos.get(linhaSelecionada).getIdProduto());
                tfNome.setText(listaDeProdutos.get(linhaSelecionada).getNome_produto());
                tfValor.setText("" + listaDeProdutos.get(linhaSelecionada).getValor_produto());

                btnEditar.setEnabled(true);
                btnExcluir.setEnabled(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro:" + e.getMessage(), "Erro ao pesquisar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tableProdutosMouseClicked

